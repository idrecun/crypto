n = 7603286354234243903435872704677498363399458016631578496018195845589487786172473
e = 7535918899271596912605330771330141519800214292622992808169830647334620913196679
M = 11111


def factorize(n):
    d = 2
    while d * d <= n:
        if n % d == 0:
            return d
        d += 1
    return None


p = factorize(n)
q = n // p
phi = (p - 1) * (q - 1)
d = pow(e, -1, phi)
S = pow(M, d, n)

print(f"S = {S}")
