def factorize(n):
    d = 2
    while d * d <= n:
        if n % d == 0:
            return d
        d += 1
    return None


p = factorize(n)
q = n // p
phi = (p - 1) * (q - 1)
d = pow(e, -1, phi)
M = pow(C, d, n)

