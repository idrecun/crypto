from kurs import xor, bits_to_bytes, bytes_to_bits
import lfsr


known_markers = [
    # JPEG: Start of Image + APP0 (JFIF)
    ("jpg", b"\xff\xd8\xff\xe0"),
    # JPEG: Start of Image + APP1 (Exif)
    ("jpg", b"\xff\xd8\xff\xe1"),
    # PNG file signature
    ("png", b"\x89PNG\r\n\x1a\n"),
    # GIF87a signature
    ("gif", b"GIF87a"),
    # GIF89a signature
    ("gif", b"GIF89a"),
]


def recover_key(ciphertext: bytes, known_plaintext: bytes) -> bytes:
    stream_part = xor(ciphertext[:2], known_plaintext[:2])
    stream_part_bits = bytes_to_bits(stream_part)
    generated_back = lfsr.lfsr_reverse(stream_part_bits, 16)
    key_bits = generated_back[:16]
    return bits_to_bytes(key_bits)


candidates = {}
for ext, marker in known_markers:
    key = recover_key(ciphertext, marker)
    plaintext = lfsr.decrypt(key, ciphertext)
    if plaintext.startswith(marker):
        candidates[key] = (plaintext, ext)

for key, (plaintext, ext) in candidates.items():
    path = f"solution.{ext}"
    with open(path, "wb") as f:
        f.write(plaintext)
    print(f"Found candidate: key={key.hex()} -> wrote {path}")
